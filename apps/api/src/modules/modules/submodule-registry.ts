/**
 * Get all submodules for a specific module
 */
export function getSubmodulesForModule(moduleCode: ModuleCode): SubmoduleDefinition[] {
  return ALL_SUBMODULES.filter((sub) => sub.parentModule === moduleCode);
}

/**
 * Get submodule by code
 */
export function getSubmoduleByCode(code: string): SubmoduleDefinition | undefined {
  return ALL_SUBMODULES.find((sub) => sub.code === code);
}

/**
 * Get template by code
 */
export function getTemplateByCode(
  code: 'SERVICES' | 'PRODUCTION' | 'TRADE',
): IndustryTemplate | undefined {
  return INDUSTRY_TEMPLATES.find((t) => t.code === code);
}

/**
 * Check if submodule dependencies are met
 */
export function checkSubmoduleDependencies(
  submoduleCode: string,
  enabledSubmodules: string[],
): { valid: boolean; missing: string[] } {
  const submodule = getSubmoduleByCode(submoduleCode);
  if (!submodule) {
    return { valid: false, missing: [] };
  }

  const missing = submodule.requiredSubmodules.filter(
    (req) => !enabledSubmodules.includes(req),
  );

  return {
    valid: missing.length === 0,
    missing,
  };
}

/**
 * Check if submodules conflict
 */
export function checkSubmoduleConflicts(
  submoduleCode: string,
  enabledSubmodules: string[],
): { hasConflict: boolean; conflicts: string[] } {
  const submodule = getSubmoduleByCode(submoduleCode);
  if (!submodule) {
    return { hasConflict: false, conflicts: [] };
  }

  const conflicts = submodule.conflictsWith.filter((conflict) =>
    enabledSubmodules.includes(conflict),
  );

  return {
    hasConflict: conflicts.length > 0,
    conflicts,
  };
}
