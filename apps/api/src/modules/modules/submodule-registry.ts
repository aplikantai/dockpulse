/**
 * SUBMODULE REGISTRY
 *
 * Centralna definicja wszystkich podmodułów w systemie DockPulse.
 * Każdy moduł może mieć wiele podmodułów, które można włączać/wyłączać per tenant.
 */

export enum ModuleCode {
  CRM = 'CRM',
  ORDERS = 'ORDERS',
  PRODUCTS = 'PRODUCTS',
  QUOTES = 'QUOTES',
  WMS = 'WMS',
  INVOICES = 'INVOICES',
  PROJECTS = 'PROJECTS',
  CALENDAR = 'CALENDAR',
  REPORTS = 'REPORTS',
  NOTIFICATIONS = 'NOTIFICATIONS',
  INTEGRATIONS = 'INTEGRATIONS',
  HR = 'HR',
  BRANCHES = 'BRANCHES',
  SUPPLIERS = 'SUPPLIERS',
}

export interface SubmoduleDefinition {
  code: string;                    // np. "ORDERS.MEASUREMENTS"
  parentModule: ModuleCode;        // np. ModuleCode.ORDERS
  name: string;                    // "Measurements"
  namePl: string;                  // "Pomiary"
  description: string;
  descriptionPl: string;
  icon: string;                    // Lucide icon name

  category: 'INCLUDED' | 'ADDON';  // W cenie modułu czy dopłata
  price: number | null;            // Cena miesięczna (null = w cenie)

  isActive: boolean;               // Czy dostępny w systemie
  isBeta: boolean;                 // Czy w wersji beta

  features: string[];              // Lista funkcji
  routes: string[];                // Frontend routes
  apiEndpoints: string[];          // Backend endpoints

  requiredSubmodules: string[];    // Zależności od innych podmodułów
  conflictsWith: string[];         // Wykluczające się podmoduły

  defaultEnabled: boolean;         // Czy włączony domyślnie z modułem
  sortOrder: number;               // Kolejność wyświetlania
}

/**
 * Template branżowy - predefiniowane zestawy modułów i podmodułów
 */
export interface IndustryTemplate {
  code: 'SERVICES' | 'PRODUCTION' | 'TRADE';
  name: string;
  namePl: string;
  description: string;
  descriptionPl: string;
  icon: string;

  // Które moduły są CORE (wymagane)
  coreModules: ModuleCode[];

  // Które moduły są ADDON (opcjonalne)
  addonModules: ModuleCode[];

  // Które podmoduły są domyślnie włączone
  defaultSubmodules: string[];

  // Które podmoduły są ukryte (nie potrzebne dla tej branży)
  hiddenSubmodules: string[];
}

